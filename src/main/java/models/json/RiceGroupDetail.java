package models.json;

public class RiceGroupDetail {
	
	private int id;
	private int riceGroupId;
	private int riceId;
	
	public RiceGroupDetail() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getRiceGroupId() {
		return riceGroupId;
	}

	public void setRiceGroupId(int riceGroupId) {
		this.riceGroupId = riceGroupId;
	}

	public int getRiceId() {
		return riceId;
	}

	public void setRiceId(int riceId) {
		this.riceId = riceId;
	}
	
	

}
