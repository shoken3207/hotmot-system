package models.json;

public class ProductCategoryBean {
	
	private int id;
	private String name;
	
	public ProductCategoryBean() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
